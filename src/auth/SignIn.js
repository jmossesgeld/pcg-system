const theme = createTheme();

export default function SignIn(props) {
  const dispatch = useDispatch();
  const navigate = useNavigate();
  const lastSession = parseInt(localStorage.getItem("lastSession"));

  const handleSubmit = (event) => {
    event.preventDefault();
    const data = new FormData(event.currentTarget);
    console.log({
      email: data.get("email"),
      password: data.get("password"),
    });
    if (data.get("email") === "admin" && data.get("password") === "12345") {
      dispatch(setIsLoggedIn(true));
      localStorage.setItem("lastSession", Date.now());
      navigate("generate");
    } else {
      alert("Invalid username/password");
    }
  };

  React.useEffect(() => {
    if (Date.now() - lastSession < 1000 * 60 * 60 * 24) {
      dispatch(setIsLoggedIn(true));
      localStorage.setItem("lastSession", Date.now());
      navigate("generate");
    } else {
      console.log("Last session expired. Please log in again.");
    }
  }, [dispatch, navigate, lastSession]);

  return (
    <ThemeProvider theme={theme}>
      <Container component="main" maxWidth="xs">
        <CssBaseline />
        <Box
          sx={{
            marginTop: 8,
            display: "flex",
            flexDirection: "column",
            alignItems: "center",
          }}
        >
          <Avatar sx={{ m: 1, bgcolor: "secondary.main" }}>
            <LockOutlinedIcon />
          </Avatar>
          <Typography component="h1" variant="h5">
            Sign in
          </Typography>
          <Box component="form" onSubmit={handleSubmit} noValidate sx={{ mt: 1 }}>
            <TextField
              margin="normal"
              required
              fullWidth
              id="email"
              label="Username"
              name="email"
              autoComplete="email"
              autoFocus
            />
            <TextField
              margin="normal"
              required
              fullWidth
              name="password"
              label="Password"
              type="password"
              id="password"
              autoComplete="current-password"
            />
            <FormControlLabel
              control={<Checkbox value="remember" color="primary" />}
              label="Remember me"
            />
            <Button type="submit" fullWidth variant="contained" sx={{ mt: 3, mb: 2 }}>
              Sign In
            </Button>
          </Box>
        </Box>
        <Copyright sx={{ mt: 8, mb: 4 }} />
      </Container>
    </ThemeProvider>
  );
}
